<?php

// WHAT IS OOP //
// This is a fundaments programming paradigm that is based on the concept of "objects"
// Code should be structured to model real-world entities and their interactions within computer programs
// Classes are created as "blueprints" for an object.

// Objects - Self contained unit that encapsulates properties and methods
// Classes - Blueprint of template to create and use an object.
// Inheritance - Allows one class to inherit properties/methods from another
// Encapsulation - Bundling the data and methods into a single unit.
// Polymorphism - Allows objects of different classes to be treated as objects of a common superclass.
// Method - This is a function that belongs to a class.

// BENEFITS OF OOP //
// 1. Modularity
// 2. Reusability
// 3. Scalability
// 4. Abstraction